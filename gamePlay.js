//initializing all gameplay segment variables
let GP_blocksArray = [];

//class declaration for the main chessboard
class GP_Chessboard
{
    //constructor function for initializing all chessboard-related variables & flags
    constructor(_GP_block_xPos, _GP_block_yPos, _GP_block_idRow, _GP_block_idCol)
    {
        this.GP_block_xPos = _GP_block_xPos;
        this.GP_block_yPos = _GP_block_yPos;
        this.GP_block_size = height/10.5;
        this.GP_block_separation = this.GP_block_size;
        this.GP_block_idRow = _GP_block_idRow;
        this.GP_block_idCol = _GP_block_idCol;
        this.GP_block_fillColor;

        this.GP_block_playerNumber = 0;
        this.GP_block_pieceType = '';
        this.GP_block_occupiedFlag = 0;
        this.GP_block_hoverFlag = 0;
        this.GP_block_selectedFlag = 0;
    }
    //function to generate individual blocks on-screen
    GP_drawBlock()
    {
        rectMode(CENTER);
        noStroke();

        //conditional statements to alternate b/w red-white fill colored blocks
        if(this.GP_block_idRow%2 == 0)
        {
            if(this.GP_block_idCol%2 != 0)
            {
                this.GP_block_fillColor = PS_redShade;
            }
            else
            {
                this.GP_block_fillColor = color(255);
            }
        }
        else
        {
            if(this.GP_block_idCol%2 == 0)
            {
                this.GP_block_fillColor = PS_redShade;
            }
            else
            {
                this.GP_block_fillColor = color(255);
            }
        }

        fill(this.GP_block_fillColor);
        rect(this.GP_block_xPos, this.GP_block_yPos, this.GP_block_size);
    }
}

//main function where everything runs
function gamePlay()
{
    //looping to copy all player piece data on chessboard from piece placement chessboard to gameplay chessboard
    for(let GP_blockRow=0; GP_blockRow<PP_chessboardSize; GP_blockRow++)
    {
        for(let GP_blockCol=0; GP_blockCol<PP_chessboardSize; GP_blockCol++)
        {
            //copying block player number, piece type, and occupied flag values from piece placement to gameplay chessboard
            GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_playerNumber = PP_blocksArray[GP_blockRow][GP_blockCol].PP_block_playerNumber;
            GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_pieceType = PP_blocksArray[GP_blockRow][GP_blockCol].PP_block_pieceType;
            GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_occupiedFlag = PP_blocksArray[GP_blockRow][GP_blockCol].PP_block_occupiedFlag;
        }
    }

    //function call for displaying on-screen text
    GP_playerTextDisplay();

    //generating chessboard and running all functionalities
    for(let GP_blockRow=0; GP_blockRow<PP_chessboardSize; GP_blockRow++)
    {
        for(let GP_blockCol=0; GP_blockCol<PP_chessboardSize; GP_blockCol++)
        {
            //function call to display individual blocks making up the chessboard
            GP_blocksArray[GP_blockRow][GP_blockCol].GP_drawBlock();

            //generating piece thumbnails on the chessboard
            if(GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_occupiedFlag == 1)
            {
                image(GP_showCardThumb(GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_pieceType, GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_playerNumber), GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_xPos, GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_yPos, GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_size, GP_blocksArray[GP_blockRow][GP_blockCol].GP_block_size);
            }
        }
    }   
}

//function to display text rendered on gameplay page
function GP_playerTextDisplay()
{
    imageMode(CORNER);
    background(PS_backgroundImage);

    //displaying the game logo on top
    GP_logoWidth = width/6;
    imageMode(CENTER);
    image(GP_gameLogo, width/2, height/12, GP_logoWidth, GP_logoWidth/4);
}

//function to generate card thumbnails depending on piece-type value found on the block
function GP_showCardThumb(letterValue, playerValue)
{
    if(playerValue == 1)
    {
        switch(letterValue)
        {
            case 'K': return PP_P1_cardThumb_K;
            break;
            case 'Q': return PP_P1_cardThumb_Q;
            break;
            case 'B': return PP_P1_cardThumb_B;
            break;
            case 'N': return PP_P1_cardThumb_N;
            break;
            case 'R': return PP_P1_cardThumb_R;
            break;
            case 'P': return PP_P1_cardThumb_P;
            break;
        }
    }
    else if(playerValue == 2)
    {
        switch(letterValue)
        {
            case 'K': return PP_P2_cardThumb_K;
            break;
            case 'Q': return PP_P2_cardThumb_Q;
            break;
            case 'B': return PP_P2_cardThumb_B;
            break;
            case 'N': return PP_P2_cardThumb_N;
            break;
            case 'R': return PP_P2_cardThumb_R;
            break;
            case 'P': return PP_P2_cardThumb_P;
            break;
        }
    }
}